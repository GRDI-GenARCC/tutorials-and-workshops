# Integers
num1 = 10
num2 = 20
print(num1 + num2)

# Floating-Point Numbers
float1 = 3.14
float2 = 2.5
print(float1 * float2)

# Strings
string1 = "Hello"
string2 = "world"
print(string1 + " " + string2)

# Booleans
is_raining = True
is_sunny = False
print(is_raining)

# Arithmetic Operators
a = 10
b = 3
remainder = a % b
print(remainder)  # Output: 1
